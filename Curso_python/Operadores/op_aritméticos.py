#Suma y resta ( + y - )
suma = 12 + 5
resta = 12 - 5

#Multiplicación y división ( * y / )
multiplicacion = 12 * 5 
division = 12 / 5 #La división siempre devuelve un número float.

#Potenciación (**)
potencia = 12 ** 5

#División bajo piso (//)
division_baja = 12 // 5 #Devuelve la parte entera de la división, redondeando hacia abajo. En este caso 2.

#Módulo (%)
modulo = 12 % 5 #Devuelve el residuo de la división. En este caso 2.

tipo_de_dato = type(5) #type(dato) devuelve el tipo de dato de la variable.
